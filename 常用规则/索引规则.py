#%%索引规则
import numpy as np
#行向量索引
b=np.array([1,2,3,4,5,6])#行向量也要加方括号
print(np.transpose(b))#行转置为列不起作用
print(b.reshape(-1,1))#重塑为列向量
print(b[3])#索引第3个字符4
print(b[:])#全行输出
print(b[:-1])#全行输出但是不包括最后1个
print(b[0:-1])#跟上述含义相同，只是省略0
print(b[0:2])#输出第0个和第1个
print(b[0:3:2])#以步长2，输出第0，2个
print(b[0:4:2])#虽然终点是4，但是不包括第4个，仍然输出第0，2个
print(b[::-1])#逆序输出全行
print(b[:-1:-1])#输出空，第0个逆序到最后1个但是又不包括
print(b[:-2:-1])#逆序输出6
print(b[:6:-1])#空，没有6号位
print(b[:5:-1])#空，有5号位，但是不包括
print(b[:4:-1])#输出6，从第0个倒退至第4个也就是5，但是不包括，只有6
print(b[:5:1])#不包括5号位
print(b[:6:1])#除了单冒号可以输出全行，只能加6号位输出全行
#%%列向量索引
h=np.array([1,2,3,4,5,6]).reshape(-1,1)#重塑为列向量
print(np.transpose(h))#列转置行起作用
print(h[:])#全列输出
print(h[:-1])#全列输出，不包括最后1个
print(h[0:2])#只输出前2个
print(h[::-1])#逆序输出全列
print(h[:4:-1])#输出6，从第0个倒退至第4个也就是5，但是不包括，只有6
#以上可以看出，所有操作对行，列没什么区别，都是从第0个到最后1个，不区分位置
#%%数组整行索引
a=np.array([[1,2,3,4,5,6,7],[4,5,6,7,8,9,10],[3,4,5,6,7,7,3],[4,5,6,7,8,8,4],[5,6,7,8,9,1,0]])
print(a[1])#数组时，索引的不是单个字符，而是整行
print(a[0,1])#逗号索引才是字符，即第0行第1列，输出2
print(a[:])#类似于全行输出，这里全数组输出
print(a[:1])#输出第0行
print(a[:2])#输出第0,1行，这说明默认操作是对行，冒号是对整行索引，不是单个字符
print(a[0:2])#输出第0，1行，只是缺省0
print(a[0:5:2])#以步长2输出第0,2,4行
print(a[::-1])#逆序输出各行
print(a[:3:-1])#输出第4行，不包括第3行
#以上操作充分说明，默认都是对行操作，对行向量同样的操作在数组格式下就是对行整体操作
#%% 数组整列索引
a=np.array([[1,2,3,4,5,6,7],[4,5,6,7,8,9,10],[3,4,5,6,7,7,3],[4,5,6,7,8,8,4],[5,6,7,8,9,1,0]])
print(a[:,1])#通过逗号隔开，实现对列操作，输出第1列
print(a[:,:])#全数组输出，任意行列
print(a[:,::-1])#按整列逆序输出
print(a[:,:5:-1])#从第0列到第5列之间逆序只有第6列，也就是最后1列
#可以看出所有操作只需要加逗号即可实现对列操作，含义都是一样的
#%%对行对列都索引，可以输出1个元素，也可以控制1行或1列，输出几个行或列元素，或者几行几列，输出相应行列的元素
a=np.array([[1,2,3,4,5,6,7],[4,5,6,7,8,9,10],[3,4,5,6,7,7,3],[4,5,6,7,8,8,4],[5,6,7,8,9,1,0]])
print(a[:3,-1])#输出第0，1，2行的最后1列
print(a[(0,1),-1])#输出第0，1行的最后1列
print(a[:1,-1])#输出第0行的最后1个，因为不包括第1行，对比可知，如果想让1能够索引第2行必须加括号引用
print(a[3,4:7:2])#输出第4行的第4，6个元素,这是以步长实现，输出[8,4]
print(a[3,(4,6)])#也可以小括号直接输入2个，分别索引，输出[8,4]，进一步能看出加括号的是从数字本身开始索引的，不会差1
print(a[(0,2),(1,3)])#输出第0行的第1个元素,第2行的第3个元素，按位置上下配对索引，不会逐个配对
#print(a[(0,1),(0,3),(2,1),(2,3)])#格式错误
print(a[0:3:2,1:4:2])#输出第0行的1，3元素和第2行的1，3元素，如果想避免孤立组队索引，应该行/列索引是连续的，用冒号，这样行位置的每个会与列的每个都配对

